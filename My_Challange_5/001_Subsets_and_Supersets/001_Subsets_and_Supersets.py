# Define sets
set_a = {1, 2, 3, 4, 5}
set_b = {2, 3, 4}

# Check if set_b is a subset of set_a
print(set_b.issubset(set_a))  # Output: True

# Check if set_a is a superset of set_b
print(set_a.issuperset(set_b))  # Output: True